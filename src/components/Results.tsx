function Marquee({ cards, direction }: { cards: ResultCard[]; direction: "left" | "right" }) {
  const animClass = direction === "left" ? "animate-marquee-left" : "animate-marquee-right";
  return (
    <div className="overflow-hidden w-full">
      <div className={`flex gap-5 py-4 w-max ${animClass}`}>
        {cards.map((card, i) => (
          <Card key={`a-${i}`} card={card} />
        ))}
        {cards.map((card, i) => (
          <Card key={`b-${i}`} card={card} />
        ))}
      </div>
    </div>
  );
}

